                        <p style="margin:0; padding:0; font-size:20px; font-weight:bold;">1.0 &nbsp;INTRODUCTION</p>
                        <p style="margin:0; padding:0; font-size:18px">Risk management practices within the Authority are anchored to the Public Finance Management Act, 2012, and its attendant 2015 Regulations 165 (1), which requires the Accounting Officer to;</p>
                        <p style="margin:0; padding:0; font-size:18px">a) &nbsp; &nbsp;Ensure that entities develop risk management strategies, which include fraud prevention mechanism; and</p>
                        <p style="margin:0; padding:0; font-size:18px">b) &nbsp; &nbsp;Develop a system of risk management and internal control that builds robust business operations.</p>
                        <p style="margin:0; padding:0; font-size:18px">The Mwongozo Code of Governance closely followed the above PFM Act for State Corporations, 2015. The second governance principle in Mwongozo calls explicitly for the Board of Directors to ensure that effective processes and systems of risk management and internal controls are in place.</p>
                        <p style="margin:0; padding:0; font-size:18px">Conversely, to support the above tenets and for good business practice, the Kenya Urban Roads Authority Board of Directors approved the Authority’s Enterprise Risk Management policy framework (30th May 2018) applicable across the processes and operations. The policy appoints the Director-General as the Chief Risk Officer accountable to the board to implement the risk management framework. The above responsibility has been appropriately delegated to Deputy Director Finance on risk matters touching on your department.</p>
                        <p style="margin:0; padding:0; font-size:18px">In addition, the approved Enterprise Risk Management policy encourages innovation and creative approaches to service delivery while requiring careful consideration of the risks involved and responding appropriately to manage them. The Authority’s Enterprise-wide Risk Management process is aimed to identify, assess, prioritize, and mitigate the significant risks that could impact the delivery of the strategic objectives. Thereafter, the Risks are prioritized for reporting in accordance with the scoring methodology in the risk management-weight matrix.</p>
                        <p style="margin:0; padding:0; font-size:18px">Strategic risks are those concerned with ensuring the overall success of the Authority’s objectives and the vitality and viability of the organization. Within the Authority, strategic risks amalgamate the various operational, project, and financial risks identified at the department level and below. The review of both strategic and department risk is undertaken on a monthly basis by the Risk Champions. Thereafter, significant risks are examined at the department level. Any risk that remains after existing controls are taken into account (residual risk) will be reported quarterly to the Enterprise Risk Management Committee (ERMC) for further consideration.</p>
